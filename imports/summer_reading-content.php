        <p><strong  class="text-info">Corresponding Writing Assignment</strong></p> <br>
        <p>You will create a writing journal for your summer reading.  For each book that you read, choose one quotation or passage from the book and write a reaction
            to it.  You might choose a quotation because it has special meaning to you or because you found it particularly
            interesting, funny, or clever.   </p><br>
        <p><strong>Each journal entry should be written on one sheet of college-rule notepaper and have the following components:</strong></p>
        <ol>
            <li>Title and Author of Book</li>
            <li>Quotation and page number</li>
            <small>If the quotation is longer than three lines, you should cut out the middle of the passage and replace it with an ellipsis (…).</small><br>
            <li>Brief synopsis of the setting for this passage</li>
            <small>Write one or two sentences describing what is taking place in the book in the part containing this passage.  This is NOT a plot summary.
                Assume that anyone reading your journal will have read the book, but just needs to be reminded of the context of this
                quotation.  </small><br>
            <li>Your reason for having chosen the quotation</li>
            <small>This should be more than one sentence- get into this part!  React to what you have read and go beyond the
                surface level.  If you’re having difficulty thinking of what to say here, you might consider some (no need to
                answer all) of the following questions: Why did you choose this quotation?  What does it mean to you?  What is
                the author trying to say?  Do you agree with the author or character?  </small><br>
            <li>Importance in the context of the book</li>
            <small>Why is this passage important within the book as a whole?  How does it enhance your understanding of the
                plot, themes, author’s message, or any other aspect of the book?</small>
        </ol>
        <p>All journal entries should be written in ink.  Do NOT double-space your entries.  Do not number your paragraphs.
            This should flow as one written piece with appropriate paragraph development and transitions between ideas.
            Proofread your writing for grammatical and spelling errors.  <strong>You should have a total of four journal entries,
            one for each book</strong> (or two for The Count of Monte Cristo if that is the only book you read from the Reader’s
            Choice list).  When you have completed all of your journal entries, you should staple or bind all of them together
            with a cover page as your first page.</p><br>
        <p>9th Grade students:  Journal entries should be ¾ to 1 full page in length.</p>
        <p>10th-12th Grade students:  Journal entries should be 1 to 1.5 pages in length.</p>

