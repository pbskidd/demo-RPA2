                    <h2>His Eminence Raymond Leo Cardinal Burke</h2>
                    <span> Cardinal Prefect of the Supreme Tribunal of the Apostolic Signatura</span>
                </div>
            </div>
            <p>His Eminence Raymond Leo Cardinal Burke came to bless Anchor Academy (Regina Pacis).</p>
            <p>His Eminence Raymond Cardinal Burke addressed the students, faculty and family of the school. Afterwards, he greeted and spoke to each faculty member and family individually. He also celebrated the daily 8 am Novus Ordo Mass at St. Mary’s Church, which was attended by the students and families.</p>
            <p>We were extraordinarily honored to have Cardinal Burke bless our schools! It is not every little school that is so honored with the blessing of a prince of the Church. Cardinal Burke offered Mass at St. Mary Church with the entire school attending and then received each of the families and the faculty in a beautiful reception that lasted several hours.</p>

            <p>In his sermon, the Cardinal encouraged the school to persevere in it's good work and affirmed Cardinal Newman Academy with these words, "You are the necessary continuation of the good accomplished at Anchor Academy." He gave us not only much encouragement but our marching orders!</p>
