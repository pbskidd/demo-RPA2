            </div>
            <p>In the Spring of 2014 our then newly appointed Bishop of Bridgeport, his excellency, Bishop Frank Caggiano came to bless Anchor Academy (Regina Pacis) and Cardinal Newman Academy. It was a day of joy; the culmination of nine years of prayer, hard work and the generosity of many, many people.</p>

            <p>The Bishop began the day by offering Mass. He spoke to the children assembled for morning prayer. He told the children, “I am absolutely delighted to be here. You have made my day and I have been waiting a long time to visit. You have the privilege to go not just to an excellent school, not just to a private school, but to go to a Catholic school. And that means you have the privilege to pray every day and to go to Mass every day because who is at the center of this school? Is it me? Is it your principal? Is it your teachers? Is it you? Who is at the center of this school? Jesus, the Lord. I thank you for Anchor Academy. I’m here to help and support you anyway I can. Our future is bright. Bring the good news to others.”</p>

            <p>We are full of gratitude for this beautiful endorsement from our Bishop.</p>
