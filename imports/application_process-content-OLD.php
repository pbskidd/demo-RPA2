        <p><strong class="text-info"><em>Non-Discrimination Policy</em></strong></p>
        <p>Regina Pacis Academy admits students of any race, color, national origin and ethnic origin to all the rights, privileges, 
        programs, and activities generally accorded or made available to students at the school. It does not discriminate on the basis 
        of race, color, national origin or ethnic origin in administration of its educational policies, admission policies, scholarship 
        programs, and athletic and other school-administered programs.</p>

        <p><strong class="text-info"><em>Admission of Non-Catholic Students</em></strong></p>
		<p>The curriculum and student life at the Regina 
        Pacis and Cardinal Newman Academies are designed primarily for the children of Roman Catholic parents. The entire curriculum is 
        permeated with Catholic philosophy. Prayer and daily Catholic devotions are an integral part of the school day. However, students 
        who are not Catholic may be admitted to the school with the explicit understanding that all students must participate in all activities 
        in the school including classes in Religion and Theology.</p>

		<p><strong class="text-info"><em>Tuition Schedule</em></strong></p>
        <p>Tuition for the academic year is $5,125 per student. Discount is available for each additional student. Families 
        enrolling 5 or more children will only be required to pay for the first four. Tuition is paid in either monthly or 
        semiannual installments.<br>An additional registration book fee of $225 (per student) is required per year.</p>
        
        <p><strong class="text-info"><em>Financial Aid</em></strong></p>
        <p>While the school’s mission is to keep tuition affordable, some families have legitimate financial difficulties at times. 
        Parents requesting financial aid are instructed by the Business Manager to fill out an online form from Student and School 
        Services, a division of National Association of Independent Schools. This organization makes an aid recommendation and submits 
        it to the school. The board’s financial aid committee considers this recommendation as well as the applicant’s most recent tax 
        return and other applicable circumstances in making its determination as to whether or not to grant or deny aid.</p>
