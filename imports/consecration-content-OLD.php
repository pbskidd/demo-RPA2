        <h3>The Consecration of Regina Pacis Academy to the Immaculate Heart of Mary.</h3>
        <br>
        <p><strong class="text-info"><em>Dear Mary, Our Mother and Our Hope </em></strong>
            , today this Solemnity of your Immaculate Conception—we consecrate ourselves and our school to your Immaculate Heart.  We ask of you this favor:  that all of us would know you sincerely and
            love you fervently.  Your Son gave you to each of us to be Our Mother, having promised that He would not leave us
            orphans. <a href="https://www.biblegateway.com/passage/?search=John%2014:18">(Jn 14:18)</a>  And so each of us desires that you shall be, from this moment, Our Mother indeed.</p><br />

        <p><strong class="text-info"><em>Your Son, Creator and Savior of the world</em></strong>,
            delighted in contemplating your beauty and your perfection.  How much do we want to venerate you and to love you more
            truly, God’s masterpiece and “our tainted nature’s solitary boast.” <a href="http://www.poetryfoundation.org/poem/174832">(Wordsworth)</a>
            In loving and serving you, do we wish to find our joy…and so we ask your Son to lend us His eyes and His heart that we would see
            you as Jesus sees you and love you as He loves you.  </p><br />

        <p><strong class="text-info"><em>O Mary</em></strong>,
            you are the most loving of mothers, and for each of us you have a watchful tenderness and a profound compassion.  Your
            love is constant.  May our thoughts and affections often turn to you as our comfort and our strength, especially when we
            are tempted or suffering.  We wish to be deeply grateful for your intercession and protection, for the special and
            personal favours you grant us. </p><br />

        <p><strong class="text-info"><em>Teach us to love the things of Heaven</em></strong>, to avoid the distraction and tangle of the here and now,
            and to conquer our pride with your humility and our lukewarmness with your zeal for the Kingdom.  When we fall into sin,
            lead us to the merciful heart of your Son who alone can restore our souls to grace and peace.</p><br />

        <p>How can we doubt your love when Jesus, who lives in us and suffers in us, will always be the desire of your heart?  Your
            part, O Mary, was to conceive Jesus in your womb, and you continue to conceive Him in the souls of all your children.
            <a href="http://www.ecatholic2000.com/montfort/true/devotion.shtml">(St Marie Grignon de Montfort)</a>  Bear Jesus
            in us and give us His life and make us saints.</p><br />

        <p><strong class="text-info"><em>Most of all, Dearest Mother</em></strong>, we ask you to give us your heart, so self-
            forgetful and so generous, that we would love Jesus as you love Him and so trust Him as you trust Him.</p><br />

        <blockquote><strong>“Mother of God’s holy Child, make me holy, too.  Keep my heart from every wrong; keep it close to you.<br>You’re
            my mother, too; I will trust in you. Make me holy like your holy Child.” </strong><small>(MC hymn)</small></blockquote>

        <p>We ask you, O Mother of Fair Love, to accept this Rosary we now offer as a pledge of our filial devotion and of our
            heartfelt desire to be known always as your children, who are at your service, abiding in your maternal affection,
            now and forever.  <br><br><strong class="text-info"><em>Amen.</em></strong>
